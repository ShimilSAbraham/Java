/* second program to demonstrate the use of variable declaration */
public class Example2
{
    public static void main(String args[])
    {
        int num = 25; //to declare a variable 'num' and assign an integer value of 25
        System.out.println("The number is "+ num);
        num *= 3;   //to change the value of num to three times itself
        System.out.println("Three times of the number is "+ num);
    }
}
